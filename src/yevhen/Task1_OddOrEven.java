package yevhen;

public class Task1_OddOrEven {

    // method that can identifies if a given number is even or odd.

    public static void oddOrEvenCheck(int number) {
        if (number % 2 == 0) {
            System.out.println(number + " is Even number");
        } else {
            System.out.println(number + " is Odd number");
        }
    }

}
